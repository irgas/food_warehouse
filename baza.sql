                lv_nrzamowienia,
                SYSDATE,
                lv_wartosc,
                pv_status,
                lv_wagazamowienia,
                '11111111111',
                pv_nrklienta
            );


--pobieranie adresu do tabeli miejsca_dostaw_hs    

            lv_pozycja_adresu   := pkg_hurtownia.f_daj_pozycje(pv_adres);
            SELECT
                nippesel,
