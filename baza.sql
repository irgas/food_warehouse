    PROCEDURE p_aktualizacja_konta (
        pv_login    klienci_hs.login%TYPE,
        pv_stare_haslo IN OUT klienci_hs.haslo%TYPE,
        pv_nowe_haslo IN OUT klienci_hs.haslo%TYPE,
        pv_email    klienci_hs.email%TYPE,
        pv_tabela   VARCHAR2
    ) IS
        lv_czy_poprawne   klienci_hs.haslo%TYPE;
    BEGIN
        IF pv_nowe_haslo IS NULL THEN
            EXECUTE IMMEDIATE 'update '
                              || pv_tabela
                              || '
                set email = :pv_email
                where upper(login) = upper(:pv_login)'
                USING pv_email,pv_login;
        ELSE
            pv_stare_haslo   := pkg_hurtownia.f_daj_hashhaslo(
                pv_login,
                pv_stare_haslo
            );
            EXECUTE IMMEDIATE 'select haslo
                                into :lv_czy_poprawne
                                    from '
                              || pv_tabela
                              || '
                                        where upper(login) = upper(:pv_login)'
              INTO lv_czy_poprawne
                USING pv_login;
            IF lv_czy_poprawne = pv_stare_haslo THEN
                pv_nowe_haslo   := pkg_hurtownia.f_daj_hashhaslo(
                    pv_login,
                    pv_nowe_haslo
                );
                EXECUTE IMMEDIATE '
                    update '
                                  || pv_tabela
                                  || '
                        set email = :pv_email, haslo = :pv_nowe_haslo, datamodyfikacji = sysdate
                        where upper(login) = upper(:pv_login)'
                    USING pv_email,pv_nowe_haslo,pv_login;
                apex_util.set_session_state(
                    'P11_KOMUNIKAT',
                    'Has³o zosta³o zmienione.'
                );
            ELSE
                apex_util.set_session_state(
                    'P11_KOMUNIKAT',
                    'Aktualne has³o jest nieprawid³owe.'
                );
            END IF;

        END IF;

        COMMIT;
    EXCEPTION
